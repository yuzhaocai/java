public class WeiBoQuoteVO {
	public WeiBoQuoteVO() {
	}

	private String weiboQuoteId;
	private String weiboType;
	private String weiboPrice;

	public String getWeiboQuoteId() {
		return weiboQuoteId;
	}

	public void setWeiboQuoteId(String weiboQuoteId) {
		this.weiboQuoteId = weiboQuoteId;
	}

	public String getWeiboType() {
		return weiboType;
	}

	public void setWeiboType(String weiboType) {
		this.weiboType = weiboType;
	}

	public String getWeiboPrice() {
		return weiboPrice;
	}

	public void setWeiboPrice(String weiboPrice) {
		this.weiboPrice = weiboPrice;
	}

}
